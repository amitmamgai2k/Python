#Operators in Python

a = 10
b = 5
print(a + b) #15
print(a - b) #5
print(a * b) #50
print(a / b) #2.0
print(a % b) #0
print(a ** b) #100000
print(a // b) #2
print(a == b) #False
print(a != b) #True
print(a > b) #True
print(a < b) #False
print(a >= b) #True
print(a <= b) #False

